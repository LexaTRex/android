public class TestResultImportException extends Exception {

    public TestResultImportException() {
    }

    public TestResultImportException(String message) {
        super(message);
    }

    public TestResultImportException(String message, Throwable cause) {
        super(message, cause);
    }

    public TestResultImportException(Throwable cause) {
        super(cause);
    }

}
